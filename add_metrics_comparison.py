# Ranking
notebook["cells"].append({
    "cell_type": "markdown",
    "id": "ranking-title",
    "metadata": {},
    "source": [
        "## 🏆 5. Method Ranking"
    ]
})

notebook["cells"].append({
    "cell_type": "code",
    "id": "ranking",
    "metadata": {},
    "execution_count": None,
    "outputs": [],
    "source": [
        "# Rank methods by each metric\n",
        "if not metrics_df.empty:\n",
        "    print('=' * 80)\n",
        "    print('🏆 METHOD RANKING BY METRICS')\n",
        "    print('=' * 80)\n",
        "    \n",
        "    # Silhouette (higher is better)\n",
        "    print('\\n1. Silhouette Score (higher is better):')\n",
        "    silhouette_ranking = metrics_df.sort_values('Silhouette', ascending=False)\n",
        "    for idx, row in silhouette_ranking.iterrows():\n",
        "        print(f'   {idx+1}. {row[\"Method\"]:<15s}: {row[\"Silhouette\"]:.4f}')\n",
        "    \n",
        "    # Davies-Bouldin (lower is better)\n",
        "    print('\\n2. Davies-Bouldin Index (lower is better):')\n",
        "    db_ranking = metrics_df.sort_values('Davies-Bouldin', ascending=True)\n",
        "    for idx, row in db_ranking.iterrows():\n",
        "        print(f'   {idx+1}. {row[\"Method\"]:<15s}: {row[\"Davies-Bouldin\"]:.4f}')\n",
        "    \n",
        "    # Calinski-Harabasz (higher is better)\n",
        "    print('\\n3. Calinski-Harabasz Score (higher is better):')\n",
        "    ch_ranking = metrics_df.sort_values('Calinski-Harabasz', ascending=False)\n",
        "    for idx, row in ch_ranking.iterrows():\n",
        "        print(f'   {idx+1}. {row[\"Method\"]:<15s}: {row[\"Calinski-Harabasz\"]:.2f}')\n",
        "    \n",
        "    # Overall ranking (rank-based scoring)\n",
        "    print('\\n' + '=' * 80)\n",
        "    print('🎯 OVERALL RANKING (Rank-based Scoring)')\n",
        "    print('=' * 80)\n",
        "    \n",
        "    scores = {method: 0 for method in metrics_df['Method']}\n",
        "    n_methods = len(metrics_df)\n",
        "    \n",
        "    # Silhouette ranking\n",
        "    for rank, row in enumerate(silhouette_ranking.iterrows(), 1):\n",
        "        scores[row[1]['Method']] += (n_methods - rank + 1)\n",
        "    \n",
        "    # Davies-Bouldin ranking\n",
        "    for rank, row in enumerate(db_ranking.iterrows(), 1):\n",
        "        scores[row[1]['Method']] += (n_methods - rank + 1)\n",
        "    \n",
        "    # Calinski-Harabasz ranking\n",
        "    for rank, row in enumerate(ch_ranking.iterrows(), 1):\n",
        "        scores[row[1]['Method']] += (n_methods - rank + 1)\n",
        "    \n",
        "    # Sort by total score\n",
        "    sorted_scores = sorted(scores.items(), key=lambda x: x[1], reverse=True)\n",
        "    \n",
        "    print(f'\\n🥇 WINNER: {sorted_scores[0][0]}')\n",
        "    print(f'   Total Score: {sorted_scores[0][1]}/{n_methods*3}\\n')\n",
        "    \n",
        "    for rank, (method, score) in enumerate(sorted_scores, 1):\n",
        "        medal = '🥇' if rank == 1 else '🥈' if rank == 2 else '🥉' if rank == 3 else '  '\n",
        "        print(f'{medal} {rank}. {method:<15s}: {score}/{n_methods*3} points')\n",
        "    \n",
        "    print('=' * 80)\n",
        "else:\n",
        "    print('⚠️  No metrics available for ranking')"
    ]
})

