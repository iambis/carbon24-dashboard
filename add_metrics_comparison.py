# Add Metrics Evaluation
notebook["cells"].append({
    "cell_type": "markdown",
    "id": "metrics-title",
    "metadata": {},
    "source": [
        "## 🎯 4. Clustering Quality Metrics\n",
        "\n",
        "Đánh giá chất lượng clustering với 3 metrics chính:\n",
        "- **Silhouette Score** (↑ higher is better): Đo độ tách biệt giữa các clusters\n",
        "- **Davies-Bouldin Index** (↓ lower is better): Đo độ compact và separation\n",
        "- **Calinski-Harabasz Score** (↑ higher is better): Đo tỷ lệ variance giữa/trong clusters"
    ]
})

notebook["cells"].append({
    "cell_type": "code",
    "id": "calculate-metrics",
    "metadata": {},
    "execution_count": None,
    "outputs": [],
    "source": [
        "# Load feature data for metrics calculation\n",
        "print('📊 Loading feature data for metrics...')\n",
        "df_features = pd.read_csv('carbon24_feature_selected/carbon24_feature_selected_standard.csv')\n",
        "\n",
        "with open('carbon24_feature_selected/selected_features.json', 'r') as f:\n",
        "    feature_info = json.load(f)\n",
        "\n",
        "numeric_features = [f for f in feature_info['numeric_features'] if f != 'relative_energy' and f in df_features.columns]\n",
        "X = df_features[numeric_features].values\n",
        "\n",
        "print(f'✅ Loaded features: {X.shape}')\n",
        "print(f'   Features used: {len(numeric_features)}')"
    ]
})

notebook["cells"].append({
    "cell_type": "code",
    "id": "compute-metrics",
    "metadata": {},
    "execution_count": None,
    "outputs": [],
    "source": [
        "# Calculate metrics for each method\n",
        "print('\\n' + '=' * 80)\n",
        "print('🎯 CLUSTERING QUALITY METRICS')\n",
        "print('=' * 80)\n",
        "\n",
        "metrics_results = []\n",
        "\n",
        "for method, data in results.items():\n",
        "    print(f'\\n{method}:')\n",
        "    print('-' * 60)\n",
        "    \n",
        "    if 'cluster' not in data['data'].columns:\n",
        "        print('  ⚠️  No cluster labels found')\n",
        "        continue\n",
        "    \n",
        "    labels = data['data']['cluster'].values\n",
        "    \n",
        "    # Filter out noise points for HDBSCAN\n",
        "    if method == 'HDBSCAN' and -1 in labels:\n",
        "        mask = labels != -1\n",
        "        X_filtered = X[mask]\n",
        "        labels_filtered = labels[mask]\n",
        "        print(f'  Filtered out {(~mask).sum()} noise points')\n",
        "    else:\n",
        "        X_filtered = X\n",
        "        labels_filtered = labels\n",
        "    \n",
        "    # Check if we have enough clusters\n",
        "    n_clusters = len(np.unique(labels_filtered))\n",
        "    if n_clusters < 2:\n",
        "        print(f'  ⚠️  Only {n_clusters} cluster(s) found, skipping metrics')\n",
        "        continue\n",
        "    \n",
        "    try:\n",
        "        # Sample for faster computation if needed\n",
        "        if len(X_filtered) > 5000:\n",
        "            sample_idx = np.random.choice(len(X_filtered), 5000, replace=False)\n",
        "            X_sample = X_filtered[sample_idx]\n",
        "            labels_sample = labels_filtered[sample_idx]\n",
        "            print(f'  Using sample of 5000 points for metrics')\n",
        "        else:\n",
        "            X_sample = X_filtered\n",
        "            labels_sample = labels_filtered\n",
        "        \n",
        "        # Calculate metrics\n",
        "        silhouette = silhouette_score(X_sample, labels_sample)\n",
        "        davies_bouldin = davies_bouldin_score(X_filtered, labels_filtered)\n",
        "        calinski = calinski_harabasz_score(X_filtered, labels_filtered)\n",
        "        \n",
        "        print(f'  Silhouette Score:      {silhouette:.4f}')\n",
        "        print(f'  Davies-Bouldin Index:  {davies_bouldin:.4f}')\n",
        "        print(f'  Calinski-Harabasz:     {calinski:.2f}')\n",
        "        \n",
        "        metrics_results.append({\n",
        "            'Method': method,\n",
        "            'Silhouette': silhouette,\n",
        "            'Davies-Bouldin': davies_bouldin,\n",
        "            'Calinski-Harabasz': calinski,\n",
        "            'N_Clusters': n_clusters\n",
        "        })\n",
        "        \n",
        "    except Exception as e:\n",
        "        print(f'  ❌ Error calculating metrics: {e}')\n",
        "\n",
        "# Create metrics DataFrame\n",
        "if metrics_results:\n",
        "    metrics_df = pd.DataFrame(metrics_results)\n",
        "    \n",
        "    print('\\n' + '=' * 80)\n",
        "    print('📊 METRICS SUMMARY')\n",
        "    print('=' * 80)\n",
        "    print(metrics_df.to_string(index=False))\n",
        "    print('=' * 80)\n",
        "else:\n",
        "    print('\\n⚠️  No metrics calculated')\n",
        "    metrics_df = pd.DataFrame()"
    ]
})

